    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String codigo = JOptionPane.showInputDialog(rootPane, "Ingrese el codigo del estudiante", "Buscar", JOptionPane.INFORMATION_MESSAGE);
        boolean encontrado = false;
        
        for (int i = 0; i < 10; i++) {

            if (aula.getEstudiantes().get(i).getCodigo().equals(codigo)) {

                Estudiante estudiante = aula.getEstudiantes().get(i);

                btGGenero.add(rbMasculino);
                btGGenero.add(rbFemenino);

                txtCodigo.setText(estudiante.getCodigo());
                txtNombre.setText(estudiante.getNombre());

                if (estudiante.getGenero() == "M") {
                    rbMasculino.setSelected(true);
                } else {
                    rbFemenino.setSelected(true);
                }

                txtPrograma.setText(estudiante.getPrograma());

                jcbSemestre.setSelectedIndex(estudiante.getSemestre() - 1);

                txtPromedio.setText(estudiante.getPromedio() + "");

                ImageIcon blaine = new ImageIcon(estudiante.getImagen());

                Icon icon = new ImageIcon(blaine.getImage().getScaledInstance(lblFotos.getWidth(), lblFotos.getHeight(), Image.SCALE_DEFAULT));

                lblFotos.setIcon(icon);

                this.repaint();

                JOptionPane.showMessageDialog(rootPane, "Estudiante encontrado!", "Informacion", JOptionPane.INFORMATION_MESSAGE);
                
                encontrado = true;
                
                break;

            }

        }
        
        if(encontrado == false){
                JOptionPane.showMessageDialog(rootPane, "Estudiante no encontrado", "Informaicion", JOptionPane.WARNING_MESSAGE);
            }
    }//GEN-LAST:event_btnBuscarActionPerformed

